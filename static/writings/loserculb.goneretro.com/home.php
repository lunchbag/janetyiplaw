<B>Another 80's Rant</B>
<P><FONT FACE="Arial" SIZE="2">Subject: To everyone who thinks the 80's were great...
<P> 
Children of the Eighties Unite</FONT>
<BR>----------------------
<P>
When I was a kid, adults used to bore me to tears with their tedious diatribes about how hard things were when they were growing up-what with walking twenty-five miles to school every morning uphill both ways through year 'round blizzards carrying their younger siblings on their backs to their one-room schoolhouse where they maintained a straight-A average despite their full-time after-school job at the local textile  mill where they worked for 35 cents an hour just to help keep their family from starving to death!
<P>
And I remember promising myself that when I grew up there was no way I was going to lay a bunch of crap like that on kids about how hard I had it and how easy they've got it!
<P>
But......
<P>
Now that I've reached the ripe old age of twenty-nine, I can't help but look around and notice the youth of today. You've got it so easy! 
<P>
I mean, compared to my childhood, you live in Utopia! And I hate to say it, but you kids today...you don't know how good you've got it! 
<P>
I mean, when I was a kid we didn't have the Internet. We wanted to know something, we had to go to the LIBRARY and look it up ourselves! 
<P>
And there was no e-mail! We had to actually write somebody a letter-with a pen! And then you had to walk all the way across the street and put it in the mailbox and it would take like a week to get there! 
<P>
And there were no MP3s or Napster! We wanted to steal music, we had to go to the record store and shoplift it ourselves! Or we had to wait around all day to tape it off the radio and the DJ would usually talk over the beginning and mess it all up! 
<P>
We didn't have fancy crap like Call Waiting! If you were on the phone and somebody else called they got a busy signal! And we didn't have fancy Caller ID Boxes either! When the phone rang, you had no idea who it was -- it could be your boss, your mom, a collections agent, your drug dealer -- you didn't know!!! You just had to pick it up and take your chances, mister! 
<P>
And we didn't have any fancy Sony Playstation or "X-Box" videogames with high-resolution 3-D graphics! We had the Atari 2600! With games like "PONG", "Space Invaders" and "Asteroids" and the graphics sucked butt! Your guy was a little square! You had to use your imagination! And there were no multiple levels or screens-it was just one screen...forever! And you could never win! The game just kept getting harder and faster until you died! Just like LIFE! 
<P>
When you went to the movie theatre there was no such thing as stadium seating! All the seats were the same height! A tall guy sat in front of you, you were screwed! 
<P>
And sure, 2% of us had cable television, but back then that was only like 20 channels and there was no onscreen menu! You had to use a little book called a TV Guide to find out what was on! And 98% of us still had to use one of those awful, frustrating antennas that you would bend and twist into every conceivable configuration AND wrap the ends with aluminium foil and still not get a clear picture! And UHF!!! Need I say more?!?!?! 
<P>
And there was no Cartoon Network! You could only get cartoons on Saturday morning...D'ya hear what I'm saying?! We had to wait ALL WEEK, you spoiled little brats!
<P>
That's exactly what I'm talking about! You kids today have got it too easy.
<P>
You're spoiled!!!
<P>
You guys wouldn't last five minutes back in 1984!!!!!
